#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*st;
	size_t			i;

	i = 0;
	st = (unsigned char *)s;
	while (i < n)
	{
		st[i] = '\0';
		i++;
	}
}
